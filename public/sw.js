
const CACHE_NAME = 'virtual-fitting-room-v1';
const urlsToCache = [
  '/',
  '/index.html',
  // Note: Add other assets like JS/CSS bundles if not inlined or loaded from CDN
];

self.addEventListener('install', (event) => {
  event.waitUntil(
    caches.open(CACHE_NAME)
      .then((cache) => {
        console.log('Opened cache');
        return cache.addAll(urlsToCache);
      })
  );
});

self.addEventListener('fetch', (event) => {
  event.respondWith(
    caches.match(event.request)
      .then((response) => {
        if (response) {
          return response;
        }
        return fetch(event.request);
      }
    )
  );
});
